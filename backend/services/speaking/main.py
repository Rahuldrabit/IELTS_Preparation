@router.post("/shadowing/model", response_model=ShadowingModelResponse)
async def get_shadowing_model(
    request: ShadowingModelRequest,
    db: AsyncSession = Depends(get_db),
):
    """
    Generate model reference audio configuration for shadowing practice.
    
    This endpoint:
    1. Generates mutation tiers (reused from ai_agent)
    2. Splits each tier into segments for rhythm tracking
    3. Returns TTS config for frontend Web Speech API playback
    
    The frontend plays the model audio while recording the user's attempt.
    """
    # Import here to avoid circular dependency
    from services.ai_agent.main import generate_mutations, MutationGenerationRequest
    
    # Generate mutation tiers using the existing AI agent
    # For now, we'll create a simple placeholder transcript
    # In production, this would be generated by AI based on the topic
    
    base_responses = {
        1: f"Well, I think {request.topic} is quite interesting. I've been interested in it for a while now.",
        2: f"I'd like to talk about {request.topic}. It's something I've been thinking about quite a lot lately. "
           f"There are several reasons why I find this topic fascinating. "
           f"First of all, it's very relevant to my daily life. "
           f"Secondly, I believe it's an important issue that affects many people. "
           f"And finally, I think understanding {request.topic} can help us make better decisions.",
        3: f"I believe {request.topic} is a complex issue that requires careful consideration from multiple perspectives.",
    }
    
    base_transcript = base_responses.get(request.part, base_responses[2])
    
    # Generate mutations
    mutation_request = MutationGenerationRequest(
        transcript=base_transcript,
        original_band=6.0,
    )
    
    try:
        mutation_result = await generate_mutations(mutation_request)
    except Exception:
        # Fallback to generated tiers
        mutation_result = _generate_fallback_tiers(base_transcript)
    
    # Convert mutation tiers to shadowing model with segment metadata
    lang_map = {
        "british": "en-GB",
        "american": "en-US",
        "australian": "en-AU",
    }
    
    tiers_config = []
    for tier in mutation_result.tiers:
        segments = _split_into_segments(tier.text)
        
        tiers_config.append(TierAudioConfig(
            tier=tier.tier,
            band_label=tier.band_label,
            target_band=tier.target_band,
            text=tier.text,
            key_changes=tier.key_changes,
            audio_hints=tier.audio_hints,
            segments=segments,
            tts_config={
                "lang": lang_map.get(request.accent, "en-GB"),
                "rate": request.speed,
                "pitch": 1.0,
            },
        ))
    
    return ShadowingModelResponse(
        topic=request.topic,
        part=request.part,
        session_id=f"shadow-{request.part}-{hash(request.topic) % 10000}",
        mutation_tiers=tiers_config,
        current_tier=1,
        pass_criteria={
            "phoneme_threshold": 0.75,
            "rhythm_threshold": 0.70,
        },
    )


