class ShadowingModelRequest(BaseModel):
    """Request to generate model reference audio for shadowing."""
    topic: str
    part: int = 2
    accent: str = "british"  # british, american, australian
    speed: float = 0.9  # TTS rate


class SegmentMeta(BaseModel):
    """Metadata for a single speech segment."""
    segment_id: int
    text: str
    word_count: int
    duration_estimate_ms: int  # Estimated duration based on word count
    pause_after_ms: int  # Recommended pause after this segment


class TierAudioConfig(BaseModel):
    """TTS configuration for a mutation tier."""
    tier: int
    band_label: str
    target_band: float
    text: str
    key_changes: list[str]
    audio_hints: str
    segments: list[SegmentMeta]
    tts_config: dict  # lang, rate, pitch for Web Speech API


class ShadowingModelResponse(BaseModel):
    """Complete shadowing session data with model reference."""
    topic: str
    part: int
    session_id: str  # Unique session identifier
    mutation_tiers: list[TierAudioConfig]
    current_tier: int = 1  # Start at tier 1
    pass_criteria: dict  # phoneme_threshold, rhythm_threshold


