class CreateExaminerSessionRequest(BaseModel):
    """Request to create a new examiner chat session."""
    part: int = 1  # IELTS Speaking part (1 or 3)
    topic: Optional[str] = None


class ExaminerChatRequest(BaseModel):
    """Request to send a message in examiner chat."""
    session_id: str
    message: str
    session_state: dict  # Serialized ExaminerSession


class ExaminerChatResponse(BaseModel):
    """Response from examiner chat."""
    session_id: str
    examiner_message: str
    follow_up_prompt: Optional[str] = None
    is_session_end: bool = False
    estimated_band: Optional[float] = None
    feedback: Optional[str] = None
    session_state: dict  # Updated session state


@router.post("/examiner/create")
async def create_examiner_session(
    request: CreateExaminerSessionRequest,
    db: AsyncSession = Depends(get_db),
):
    """
    Create a new examiner chat session.
    Returns opening message and session state.
    """
    from services.agents.examiner.agent import create_session
    
    try:
        session = create_session(part=request.part, topic=request.topic)
        
        return {
            "session_id": session.session_id,
            "part": session.part,
            "topic": session.topic,
            "opening_message": session.messages[0].content if session.messages else "",
            "session_state": session.model_dump(),
        }
    except Exception as e:
        raise HTTPException(status_code=500, detail=f"Failed to create session: {str(e)}")


@router.post("/examiner/chat", response_model=ExaminerChatResponse)
async def examiner_chat(
    request: ExaminerChatRequest,
    db: AsyncSession = Depends(get_db),
):
    """
    Send a message to the examiner and get a response.
    The examiner generates natural follow-up questions based on the student's answer.
    """
    from services.agents.examiner.agent import (
        ExaminerSession,
        process_student_response,
    )
    
    try:
        # Reconstruct session from state
        session = ExaminerSession.model_validate(request.session_state)
        
        # Process student's response
        response = await process_student_response(session, request.message)
        
        return ExaminerChatResponse(
            session_id=session.session_id,
            examiner_message=response.message,
            follow_up_prompt=response.follow_up_prompt,
            is_session_end=response.is_session_end,
            estimated_band=response.estimated_band,
            feedback=response.feedback,
            session_state=session.model_dump(),
        )
    except Exception as e:
        raise HTTPException(status_code=500, detail=f"Chat failed: {str(e)}")


