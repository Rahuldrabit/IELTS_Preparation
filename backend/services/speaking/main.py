def _split_into_segments(text: str, max_words: int = 8) -> list[SegmentMeta]:
    """
    Split text into segments for rhythm tracking.
    Each segment is a phrase or clause that should be spoken as a unit.
    """
    words = text.split()
    segments = []
    segment_id = 0
    
    # Split on punctuation boundaries first
    import re
    # Split on sentence boundaries (. ! ?) and clause boundaries (, ; :)
    raw_segments = re.split(r'(?<=[.!?,;:])\s+', text)
    
    for raw_seg in raw_segments:
        seg_words = raw_seg.split()
        word_count = len(seg_words)
        if word_count == 0:
            continue
            
        # Estimate duration: ~150ms per word at normal speed
        duration_estimate = int(word_count * 150 / 0.9)  # Adjust for TTS rate
        pause_after = 200 if raw_seg.rstrip().endswith(('.', '!', '?')) else 100
        
        segments.append(SegmentMeta(
            segment_id=segment_id,
            text=raw_seg.strip(),
            word_count=word_count,
            duration_estimate_ms=duration_estimate,
            pause_after_ms=pause_after,
        ))
        segment_id += 1
    
    return segments


